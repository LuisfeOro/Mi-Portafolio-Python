numeroUno = float(input("Ingrese un numero: "))
numeroDos = float(input("Ingrese otro numero: "))
print(f"Las division al piso de {numeroUno} entre {numeroDos} es: {(numeroUno // numeroDos)}")

#para numero de decimales. DIVISION APROXIMADA HACIA ABAJO,
#Para escribir el valor con 2 decimales utilizamos el valor: .2f
#Para escribir el valor con 3 decimales utilizamos el valor: .3f
print(f"Las division al piso de {numeroUno} entre {numeroDos} es: {(numeroUno // numeroDos): .2f}")