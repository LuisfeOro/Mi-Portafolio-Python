print("Conjuncion")
print(f"{True} y {True} : {True and True}")
print(f"{True} y {False} : {True and False}")
print(f"{False} y {True} : {False and True}")
print(f"{False} y {False} : {False and False}")

print("Disyuncion")
print(f"{True} y {True} : {True or True}")
print(f"{True} y {False} : {True or False}")
print(f"{False} y {True} : {False or True}")
print(f"{False} y {False} : {False or False}")

#Negacion y afiramcion
print(not(False))
print(not(True))